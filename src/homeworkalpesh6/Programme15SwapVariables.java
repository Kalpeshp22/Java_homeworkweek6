package homeworkalpesh6;

public class Programme15SwapVariables {
    public static void main(String[] args) {
        // set the initial values of the variables a, b, c
        int a =50;
        int b = 75;
        int c;
        // overwrite the initial values of the variables
        c = a;
        a = b;
        b = c;
        // print the value of the changed variables
        System.out.println("a=");
        System.out.println(a);
        System.out.println("b=");
        System.out.println(b);
    }


}
